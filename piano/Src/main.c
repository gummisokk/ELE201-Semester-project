/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */

  volatile uint32_t timer_val;

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_DAC_Init();
  MX_TIM2_Init();
  MX_ADC1_Init();
  /* USER CODE BEGIN 2 */

  HAL_DAC_Start(&hdac, DAC_CHANNEL_1);
  HAL_ADC_Start_DMA(&hadc1, (uint32_t*)pot_value, 1);
  HAL_TIM_Base_Start(&htim2);

  // Get current time (microseconds)
  timer_val = __HAL_TIM_GET_COUNTER(&htim2);

  uint16_t volume = 2047;

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
    timer_val = __HAL_TIM_GET_COUNTER(&htim2);
    double signal = 0;
    double x = (double)(timer_val / 1000000.0); //timer in seconds 
    double factor = (double)2.0 * M_PI * x;
    int notes = 0;

    //get pot value
    uint32_t pot = pot_value[0];
    double detune = 0.5 + (double)(pot_value[0]/4095.0f);
    detune = round(detune *100.0)/100;

    factor *= detune;
    
    if (HAL_GPIO_ReadPin(KEY_3_GPIO_Port, KEY_3_Pin)){ //KEY ON THE FAR LEFT
      //A's an octive apart
      signal += (double)1.0 + sin(factor * 440.0);//440hz
      signal += (double)1.0 + sin(factor * 880.0);//880hz
      //signal += (double)1.0 + sin(factor * 1760.0);//17600hz
      notes++;
    }

    if (HAL_GPIO_ReadPin(KEY_6_GPIO_Port, KEY_6_Pin)){ //next key
      //C
      signal += (double)1.0 + sin(factor * 523.25);
      signal += (double)1.0 + sin(factor * 1046.50);
      notes++;
    }
    
    if (HAL_GPIO_ReadPin(KEY_4_GPIO_Port, KEY_4_Pin)){
      //D
      signal += (double)1.0 + sin(factor * 587.33);
      signal += (double)1.0 + sin(factor * 1174.66);
      notes++;
    }

    if (HAL_GPIO_ReadPin(KEY_5_GPIO_Port, KEY_5_Pin)){
      //E
      signal += (double)1.0 + sin(factor * 659.26);
      signal += (double)1.0 + sin(factor * 1318.51);
      notes++;
    }

    if (HAL_GPIO_ReadPin(KEY_1_GPIO_Port, KEY_1_Pin)){
      //G
      //signal += (double)1.0 + sin(factor * 392.00);
      signal += (double)1.0 + sin(factor * 783.99);
      signal += (double)1.0 + sin(factor * 1567.98);
      notes++;
    }

    if (HAL_GPIO_ReadPin(KEY_2_GPIO_Port, KEY_2_Pin)){
      //A igjen
      //signal += (double)1.0 + sin(factor * 440.0);
      signal += (double)1.0 + sin(factor * 880.0);
      signal += (double)1.0 + sin(factor * 1760.0);
      notes++;
    }

    if (HAL_GPIO_ReadPin(KEY_0_GPIO_Port, KEY_0_Pin)){
      //C igjen
      //signal += (double)1.0 + sin(factor * 523.25);
      signal += (double)1.0 + sin(factor * 1046.50);
      signal += (double)1.0 + sin(factor * 2093.00);

      notes++;
    }

    signal *= volume/notes; //"average" out the volume so it doesn't distort

    HAL_DAC_SetValue(&hdac, DAC_CHANNEL_1, DAC_ALIGN_12B_R, (uint16_t)signal);
  }
  /* USER CODE END 3 */
}

